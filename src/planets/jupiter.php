				<h1>Jupiter</h1>
			   	<p>Jupiter is the fifth planet from the Sun. It is the largest planet in our Solar System. Even though it is far from the Sun and Earth, its size and its cloud cover gives it a bright appearance.</br></br>

Jupiter is covered by a gaseous cloud cover. Jupiter rotates on its axis faster than any other planet in the Solar System â€” about two and a half times every Earth day. Combined with its gaseous cloud cover, this results in permanent atmospheric storms. The most notable of these storms causes the "Red Spot."</br></br>

Jupiter has at least 16 moons, plus a gaseous ring that could contain other moons. The four largest moons can be seen with a small telescope. They were first discovered by Galileo Galilei in 1610, and are called the Galilean Moons: Europa, Callisto, Ganymede, and Io. Galileo discovered these moons with his newly developed telescope. These were the first moons ever observed orbiting another planet, and showed that not all objects revolved around the Earth or Sun.</br></br>

Of these four moons, Ganymede is the largest in the Solar System, Callisto is the third-largest in the Solar System, and Io is the fourth-largest. (The second-largest moon in the Solar System is Saturn's moon Titan.) The smaller moons are the size of asteroids, and were discovered in the 1900s with more powerful telescopes. Jupiter's moons are (from nearest to furthest from the planet): Metis, Adrastea, Almathea, Thebe, Io, Europa, Ganymede, Callisto, Leda, Himalia, Lysithea, Elara, Ananke, Carme, Pasiphae, and Sinope.</br></br>

Astronomers had a unique opportunity to observe a comet striking a planet in July 1994 when the Shoemaker-Levy Comet struck Jupiter. Before the comet struck, Jupiter's powerful gravity broke it into 21 pieces. The impacts made dark spots on Jupiter's surface that could be seen by thousands of amateur astronomers on Earth. Some of these black spots were larger than the Earth. After several months, the powerful wind storms on Jupiter's surface covered these black spots.</br></br>

Ancient Greeks and Romans named Juptier after their mythological god of greatness, Jove.</br></br></p>
