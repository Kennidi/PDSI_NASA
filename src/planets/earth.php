				<span>
				The Earth, third planet from the Sun, is our home planet. We rotate around the Sun in about 365.24 days. Some ancients believed that the planets (including our Earth) revolved around the Sun. However, many thought the planets and the Sun revolved around the Earth. Today we know that the Earth, like all the other planets in our Solar System, revolves around the Sun. (Technically, everything in the Solar System revolves around the Barycenter, the center of gravity of the Solar System that is near the Sun.) The photograph at left was taken by the Apollo 17 crew shortly after liftoff. Apollo 17 was the last of the Apollo missions to the Moon.

The Earth is a little larger at its Equator than at the North and South Poles. It spins on an axis tilted about 23.5 degrees from the plane (two-dimensional path) in which it orbits around the Sun. We are approximately 93,000,000 miles from the Sun. This distance is known as an Astronomical Unit. Because distances in the Solar System are large, we often use large measures such as an Astronomical Unit to describe those distances.
				
