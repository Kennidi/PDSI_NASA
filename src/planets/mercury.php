				<span>Mercury is the closest planet to the Sun, so the Sun's gravity attracts it the most. As a result, Mercury orbits around the Sun faster than any other planet. Mercury orbits the Sun in about 88 days.

Mercury is so close to the Sun, and the Sun is so bright, that we can only see Mercury just before sunrise or just after sunset. When it is visible, Mercury always appears just above the horizon.

Because Mercury travels so fast across the sky, the ancient Greeks and Romans named it after their mythological Messenger god.
				
